const POSITION_TABLES: Record<PieceSymbol, number[]> = {
  p: PAWN_TABLE,
  n: KNIGHT_TABLE,
  b: BISHOP_TABLE,
  r: ROOK_TABLE,
  q: QUEEN_TABLE,
  k: KING_TABLE,
};

function getPositionValue(piece: PieceSymbol, color: Color, row: number, col: number): number {
  const table = POSITION_TABLES[piece];
  // For black, mirror the table vertically
  const idx = color === 'w' ? row * 8 + col : (7 - row) * 8 + col;
  return table[idx];
}

function evaluate(chess: Chess): number {
  const board = chess.board();
  let score = 0;

  for (let row = 0; row < 8; row++) {
    for (let col = 0; col < 8; col++) {
      const piece = board[row][col];
      if (!piece) continue;

      const material = PIECE_VALUES[piece.type];
      const positional = getPositionValue(piece.type, piece.color, row, col);
      const value = material + positional;

      score += piece.color === 'w' ? value : -value;
    }
  }

  return score;
}

function minimax(
  chess: Chess,
  depth: number,
  alpha: number,
  beta: number,
  isMaximizing: boolean,
): number {
  if (depth === 0 || chess.isGameOver()) {
    return evaluate(chess);
  }

  const moves = chess.moves();

  if (isMaximizing) {
    let maxEval = -Infinity;
    for (const move of moves) {
      chess.move(move);
      const evalScore = minimax(chess, depth - 1, alpha, beta, false);
      chess.undo();
      maxEval = Math.max(maxEval, evalScore);
      alpha = Math.max(alpha, evalScore);
      if (beta <= alpha) break;
    }
    return maxEval;
  } else {
    let minEval = Infinity;
    for (const move of moves) {
      chess.move(move);
      const evalScore = minimax(chess, depth - 1, alpha, beta, true);
      chess.undo();
      minEval = Math.min(minEval, evalScore);
      beta = Math.min(beta, evalScore);
      if (beta <= alpha) break;
    }
    return minEval;
  }
}

export function findBestMove(chess: Chess): string | null {
  const moves = chess.moves();
  if (moves.length === 0) return null;

  const isWhite = chess.turn() === 'w';
  let bestMove = moves[0];
  let bestEval = isWhite ? -Infinity : Infinity;

  for (const move of moves) {
    chess.move(move);
    const evalScore = minimax(chess, 2, -Infinity, Infinity, !isWhite);
    chess.undo();

    if (isWhite) {
      if (evalScore > bestEval) {
        bestEval = evalScore;
        bestMove = move;
      }
    } else {
      if (evalScore < bestEval) {
        bestEval = evalScore;
        bestMove = move;
      }
    }
  }

  return bestMove;
}
